def check_firewall():
    """Verifica configura√ß√£o do firewall"""
    print("üî• 3. VERIFICANDO FIREWALL...")
    
    checks = []
    
    # Verificar se UFW est√° instalado
    success, output, error = run_command("which ufw")
    if success:
        checks.append(("‚úÖ", "UFW instalado"))
        
        # Verificar status do UFW
        success, output, error = run_command("sudo ufw status")
        if "Status: active" in output:
            checks.append(("‚úÖ", "UFW ativo"))
            
            # Verificar regras b√°sicas
            if "22/tcp" in output or "ssh" in output:
                checks.append(("‚úÖ", "SSH permitido"))
            else:
                checks.append(("‚ö†Ô∏è", "SSH n√£o configurado no firewall"))
            
            if "80/tcp" in output and "443/tcp" in output:
                checks.append(("‚úÖ", "HTTP/HTTPS permitidos"))
            else:
                checks.append(("‚ùå", "HTTP/HTTPS n√£o configurados"))
                
        else:
            checks.append(("‚ùå", "UFW n√£o est√° ativo"))
    else:
        checks.append(("‚ùå", "UFW n√£o instalado"))
    
    # Verificar Fail2Ban
    success, output, error = run_command("which fail2ban-server")
    if success:
        checks.append(("‚úÖ", "Fail2Ban instalado"))
        
        success, output, error = run_command("sudo fail2ban-client status")
        if success and "SSH" in output.upper():
            checks.append(("‚úÖ", "Fail2Ban protegendo SSH"))
        else:
            checks.append(("‚ùå", "Fail2Ban n√£o configurado para SSH"))
    else:
        checks.append(("‚ùå", "Fail2Ban n√£o instalado"))
    
    # Verificar script de configura√ß√£o
